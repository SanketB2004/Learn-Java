
// This program demonstrates how to show output in Java.
public class OutputExample {  // Changed class name to 'OutputExample'

    // Main method: This is the entry point of the program.
    public static void main(String[] args) {

        // print(): Prints text without a newline.
        System.out.print("One");
        System.out.print("One");
        System.out.println("One");  // println(): Prints text and moves to the next line.

        // Using println() to print each output on a new line.
        System.out.println("One");
        System.out.println("One");
        System.out.println("One");

        // Example of using escape character \n for a new line.
        System.out.print("Two\n");  // Prints "Two" and moves to the next line.
        System.out.print("Three\n");  // Prints "Three" and moves to the next line.
    }
}
