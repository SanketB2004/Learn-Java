
// Main class to demonstrate Java output and patterns
public class Main {
    public static void main(String[] args) {
        
        // 1. Simple Greeting
        System.out.println("Good Morning!");

        // 2. Showing Output Example
        System.out.print("One ");
        System.out.print("One ");
        System.out.print("One ");
        System.out.println("One");  // New line after previous prints
        System.out.println("One");
        System.out.println("One");
        System.out.println("One");

        // 3. Star Patterns

        // Pattern 1: Increasing Triangle
        System.out.println("\nPattern 1:");
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");

        // Pattern 2: Decreasing Triangle
        System.out.println("\nPattern 2:");
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*");

        // Pattern 3: Pyramid Style
        System.out.println("\nPattern 3:");
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");

        // Final Message
        System.out.println("\nHello World!");
    }
}

