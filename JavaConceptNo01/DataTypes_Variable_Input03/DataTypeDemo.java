package JavaConceptNo01.DataTypes_Variable_Input03;

public class DataTypeDemo {

    public static void main(String[] args) {
       
        // Integer Data Type Example
        int myIntt = 12;  // Stores whole numbers
        System.out.println("Integer value: " + myIntt + " 02");

        // Short Data Type Example
        short myShortt;
        myShortt = 455;  // Stores smaller whole numbers
        System.out.println("Short value (first): " + myShortt + " 02");
        myShortt = 555;  // Updated value
        System.out.println("Short value (updated): " + myShortt + " 02");

        // Boolean Data Type Example
        boolean myBooleann;
        myBooleann = false;  // Stores true/false values
        System.out.println("Boolean value: " + myBooleann + " 02");

        // Float Data Type Example
        float myFloatt;
        myFloatt = 5.0F;  // Stores decimal numbers with less precision
        System.out.println("Float value: " + myFloatt + " 02");

        // Double Data Type Example
        double myDouble;
        myDouble = 5.0d;  // Stores decimal numbers with higher precision
        System.out.println("Double value: " + myDouble + " 02");

        // String Data Type Example
        String myString;
        myString = "Good Morning";  // Stores sequence of characters
        System.out.println("String value: " + myString + " 02");
    }
}
