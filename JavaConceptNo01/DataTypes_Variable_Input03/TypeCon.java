package JavaConceptNo01.DataTypes_Variable_Input03;
public class TypeCon {

    public static void main(String[] args) {
        
        // Correct float assignment with casting
        float myU = (float) 4L;
        System.out.println("Float value: " + myU);

        // Correct Short assignment with double to int to short conversion
        Short myUU = (short) 22.00;
        System.out.println("Short value: " + myUU);
    }
}
