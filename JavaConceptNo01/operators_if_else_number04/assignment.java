package JavaConceptNo01.operators_if_else_number04;

public class assignment {

    public static void main(String[] args) {
        
        // Declare an integer variable 'myInt' and assign it the value 9
        int myInt = 9;
        System.out.println(myInt);  // Output the value of 'myInt'

        // Assign the value of 'myInt' to a new variable 'newInt'
        int newInt = myInt;
        System.out.println(newInt);  // Output the value of 'newInt'

        // Calculate the sum of 'myInt' and 'newInt'
        int sum = myInt + newInt;
        
        // Print the result of the addition
        System.out.println(sum);  // Expected output: 18 (9 + 9)
    }
}
