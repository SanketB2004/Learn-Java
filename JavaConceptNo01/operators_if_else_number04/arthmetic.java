package JavaConceptNo01.operators_if_else_number04;

public class arthmetic {

    public static void main(String[] args) {
        
        // Declare two integer variables
        int a = 50;
        int b = 100;

        // Perform basic arithmetic operations with integers
        System.out.println(a + b);  // Addition: 50 + 100 = 150
        System.out.println(a - b);  // Subtraction: 50 - 100 = -50
        System.out.println(a * b);  // Multiplication: 50 * 100 = 5000
        System.out.println(a / b);  // Division (integer): 50 / 100 = 0 (because both are integers)

        // Declare two double variables
        double x = 8;
        double y = 5;

        // Perform modulus operation (remainder after division)
        System.out.println(x % y);  // Modulus: 8 % 5 = 3 (remainder when 8 is divided by 5)
    }
}
