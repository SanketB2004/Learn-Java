package JavaConceptNo01.challenges;
// show the following star patterns for use \n ?
public class cha04 {
    public static void main(String[] args) {
        // 1st print statement: First three lines
        System.out.println("*\n* *\n* * *");

        // 2nd print statement: Reverse three lines
        System.out.println("\n* * *\n* *\n*");

        // 3rd print statement: Pyramid shape
        System.out.println("\n  *\n * *\n* * *");
    }
}
